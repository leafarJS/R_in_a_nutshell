# ARRAYS

#An array is an extension of a vector to more than two dimensions. Arrays are used
#to represent multidimensional data of a single type. As above, you can generate an
#array with the array function:

a <- array(data = 1:24, 
           dim = c(2,4,3))
a


a <- array(data = 1:24, 
           dim = c(3,4,2))
a
